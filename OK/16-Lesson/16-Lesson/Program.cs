using System;
namespace _16_Lesson
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 0; i <= 10; i += 2)
            {
                Console.WriteLine(i);
            }

            for (int i = 10; i > 0; i--)
            {
                Console.WriteLine(i);
            }
            Console.WriteLine("Happy Ramadan!");
        }
    }
}